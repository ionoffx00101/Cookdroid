public class Point {
  private int x;
  private int y;
  private boolean isDraw;

  public int getX() {
    return x;
  }

  public int getY() {
    return y;
  }

  public void setIsDraw(boolean draw) {
    isDraw = draw;
  }

  public void setX(int x) {
    this.x = x;
  }

  public void setY(int y) {
    this.y = y;
  }

  public boolean getIsDraw() {
    return isDraw;
  }
}
